/**
 * Get the display values for a song based on the current language.
 * Falls back to Chinese fields if no English translation exists.
 */
export function getSongDisplay(song, lang) {
  const tr = songTranslations[song.title] ?? {};
  if (lang === "en") {
    return {
      title:            tr.titleEn            ?? song.title,
      artist:           tr.artistEn           ?? song.artist,
      lyrics:           tr.lyricsEn           ?? song.lyrics,
      lyricsTimestamps: tr.lyricsTimestampsEn ?? song.lyricsTimestamps,
    };
  }
  return {
    title:            song.title,
    artist:           song.artist,
    lyrics:           song.lyrics,
    lyricsTimestamps: song.lyricsTimestamps,
  };
}
